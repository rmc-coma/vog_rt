#include "ft_string.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *s1_cp;

	s1_cp = s1;
	while (*s1 != '\0')
		++s1;
	ft_strcpy(s1, s2);
	return (s1_cp);
}
